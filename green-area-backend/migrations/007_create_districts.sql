-- composite FK: ตารางข้อมูลอำเภอ → districts(province, name_en)  (idempotent)
DO $$
DECLARE
  t TEXT;
  tables TEXT[] := ARRAY['district_ndvi_annual','district_ndvi_monthly',
                         'district_lst_annual','district_lst_monthly'];
  cname TEXT;
BEGIN
  FOREACH t IN ARRAY tables LOOP
    cname := 'fk_' || t || '_district';
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = cname) THEN
      EXECUTE format(
        'ALTER TABLE %I ADD CONSTRAINT %I FOREIGN KEY (province, district) '
        'REFERENCES districts(province, name_en)', t, cname);
    END IF;
  END LOOP;
END $$;

CREATE INDEX IF NOT EXISTS idx_districts_province ON districts(province);
